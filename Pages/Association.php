        <p> Nous nous sommes renseignés sur l'existence d'associations pour aider les personnes atteintes d'un handicap. Nous avons été surpris de voir le nombre d'associations existantes pour faciliter le divertissement chez les personnes atteintes de handicap. Nous avons aussi trouvé des lois écrites pour aider les personnes handicapées à se divertir. Voici quelques associations et lois trouvées :<br>

            <br>  -<strong>CapGame :</strong> C'est en 2013 que Jérôme Dupire et 3 autres personnes décident de créer cette association suite à leurs nombreux questionnement sur l'équité entre les personnes atteintes d'un handicap et les personnes n'en ayant pas, sur les jeux vidéo. Ils en sont arrivés au même constat: l'accessibilité aux jeux vidéo pour les personnes handicapées est plus compliquée mais elle reste possible. Cette association a été créée dans le but de trouver des solutions materielles et logicielles pour rendre l'accessibilité plus simple.<br>

            <br> -<strong>ASHJP :</strong> L’Association Sportive Handi-Jeunes de Paris est une association qui propose des activités sportives aux jeunes atteints de handicap dans des lieux où le déplacement pour des personnes à mobilité réduite est plus simple.<br>

            <br> -<strong>La CVAA :</strong> La CVAA (21st Century Communication and Video Accessibility Act) n'est pas une association, mais un texte de lois très important dans le monde du jeux vidéo. Ce texte a été présenté par le gouvernement de Barack Obama en 2010, mais n'est entré en vigueur qu'à partir de 2018, dans le but d'améliorer l'accessibilité de toutes les technologies d'information et de communication, mais aussi des jeux vidéos. C'est à dire que les développeurs doivent respecter certaines consignes pour le développement de leurs jeux sous peine d'une poursuite judiciaire. Par exemple faire en sorte que les systèmes de communication entre les joueurs soient accessibles aux personnes atteintes de n'importe quel handicap.<br>

